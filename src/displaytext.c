/**
 * @Author: PengJL
 * @Description: 在LCD屏幕的(x0,y0)坐标处，
 * 显示宽度为width，高度为height的word字符
 * @param {char} word: 需要显示的字符
 * @param {int} x0: 字符word左上角的x轴坐标
 * @param {int} y0: 字符word左上角的y轴坐标
 * @param {int} width: 字符的宽度
 * @param {int} height: 字符的高度
 * @param {char} pa: 显示字符的属性 0:不遮盖背景，1:遮盖背景
 * @return {*} 无返回值
 */
void display_text(const char word[], int x0, int y0, int width, int height, char pa)
{
    int dot, x , y;
    
    for( dot = 0; dot < width * height; dot++)
    {
        int index, bit;

        x = x0 + dot%width;
        y = y0 + dot/width;

        index = dot / 8; //找到像素点在字符数组中的索引
        bit = 7 - dot % 8;   //找到像素点是字符内的第几个位
        
        if(word[index] & (1<<bit))
        {
            lcd_draw_point(x,y,0x000000);
        }else{
            if(pa == 1)
            {
                lcd_draw_point(x,y,0xffffff);
            }
            
        }
    }

}


